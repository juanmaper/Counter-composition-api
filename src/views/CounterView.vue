<template>
  <h1>CounterView</h1>
  <h2>{{ counter }}</h2>

  <button @click="increase">+1</button>
  <button @click="decrease">-1</button>

</template>

<script>
import { ref } from '@vue/reactivity'

export default {
  name: 'Counter',
  props: {},
  emits: [],
  setup() {

    const counter = ref(5)

    // const increase = () => {
    //   counter.value++
    // }

    // const decrease = () => {
    //   counter.value--
    // }

    return {
      counter,

      decrease: () => counter.value--,
      increase: () => counter.value++
    }
  }
}
</script>

